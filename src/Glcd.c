void initLcd(void)
{	
	int i, CONTROL[4] = {0x40,0xB8,0xFF,0x3F};


	PORTDbits.RD5 = 0;
	PORTBbits.RB15 = 0;
	PORTF = 0x01;
	PORTDbits.RD7 = 0;
	PORTDbits.RD7 = 1;
	PORTF = 0x02;
	PORTDbits.RD7 = 0;
	PORTDbits.RD7 = 1;

	PORTFbits.RF8 = 1;

	for(i = 0;i < 4;i++)
	{
		PORTE = CONTROL[i];
		PORTF = 0x01;
		Trigger();
		busy();
		PORTF = 0x02;
		Trigger();
		busy();	
	}
	PORTFbits.RF8 = 1;
}
