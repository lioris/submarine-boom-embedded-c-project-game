void writeXY(unsigned int x,unsigned int y,int lcd_cs)
{
	PORTDbits.RD5 = 0;
	PORTBbits.RB15 = 0;
	PORTF = lcd_cs;
	PORTE = 0xB8 | x;
	Trigger();
	busy();
	PORTE = 0x40 | y;
	Trigger();
	busy();
	//PORTFbits.RF8 = 1;
}
